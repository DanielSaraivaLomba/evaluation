<template>
  <v-app id="inspire">
    <v-content>
      <v-container fluid fill-height>
        <v-layout align-center justify-center>
          <v-flex xs12 sm8 md4>
            <v-card class="elevation-12">
              <v-toolbar dark color="primary">
                <v-toolbar-title>Login</v-toolbar-title>
                <v-spacer></v-spacer>
              </v-toolbar>
              <v-card-text>
                <v-form>
                  <v-text-field prepend-icon="person" type="text" name="username" v-model="input.username" placeholder="Username"></v-text-field>
                  <v-text-field id="password" prepend-icon="lock" v-model="input.password" placeholder="Password" name="password" label="Password" type="password"></v-text-field>
                </v-form>
              </v-card-text>
              <v-card-actions>
                <v-spacer></v-spacer>
                <v-btn type="button" v-on:click="login()">Login</v-btn>
              </v-card-actions>
            </v-card>
          </v-flex>
        </v-layout>
      </v-container>
    </v-content>
  </v-app>
</template>



<script>
export default {
  name: 'Login',
  data () {
    return {
      input: {
        username: '',
        password: ''
      }
    }
  },
  methods: {
    login () {
      if (this.input.username !== '' && this.input.password !== '') {

        if (this.input.username === 'evaluator' && this.input.password === 'evaluator') {
          this.$emit('authenticated', true)
          sessionStorage.setItem('evaluatorID', 'evaluatorID')
          this.$router.replace('/home')
        }
         if (this.input.username === 'teacher' && this.input.password === 'teacher') {
          this.$emit('authenticated', true)
          this.$router.replace('/home')
        } else {
          console.log('The username and / or password is incorrect')
        }
      } else {
        console.log('A username and password must be present')
      }
    }
  }
}
</script>

